namespace SnapperCodingChallenge.Core
{
    public struct Coordinate
    {
        public double X;
        public double Y;
           
        public Coordinate(double x, double y)
        {
            X = x;
            Y = y;
        }

        public Coordinate(int x, int y)
        {
            X = x;
            Y = y;
        }


    }
}
